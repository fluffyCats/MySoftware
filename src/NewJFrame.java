    public int findThePosition() {
        Grid tempGrid = null;
        int result = 0;
        do {
            if (isGameOver()) {
                JOptionPane.showMessageDialog(null, "Sorry~ 游戏结束咯~");
                System.exit(0);
            }
            int a = (int) (Math.random() * 10);
            int b = (int) (Math.random() * 10);
            Point tempPoint = new Point(a, b);
            for (int i = 0; i <= 99; i++) {
                if (((Grid) buttonList.get(i)).coodrinate.equals(tempPoint)) {
                    tempGrid = (Grid) buttonList.get(i);
                    result = i;
                    break;
                }
            }
        } while (tempGrid.ball != null);
        return result;
    }

    public boolean isGameOver() {
        for (int i = 0; i <= 99; i++) {
            if (((Grid) buttonList.get(i)).ball == null) {
                return false;
            }
        }
        return true;
    }

    public int[] massArray() {
        int[] array = new int[100];
        for (int i = 0; i <= 99; i++) {
            array[i] = i;
        }
        for (int i = 0; i < 200; i++) {
            int a = (int) (Math.random() * 100);
            int b = (int) (Math.random() * 100);
            int temp;
            temp = array[a];
            array[a] = array[b];
            array[b] = temp;
        }
        return array;
    }//返回乱序数组
